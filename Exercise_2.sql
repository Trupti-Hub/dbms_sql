CREATE TABLE Salespeople (
        S_NO NUMBER(10) PRIMARY KEY,
        SNAME VARCHAR(50),
        CITY VARCHAR(50) CONSTRAINT SALESPEOPLE_NOT_BELONG_TO_PESHAVAR CHECK(UPPER (CITY) != 'PESHAVAR'),
        COMM NUMBER(10)
);

CREATE TABLE Customer (
        CUST_NO VARCHAR2(5) PRIMARY KEY CONSTRAINT Cust_No_MUST_START_WITH_C_AND_MUST_CONTAINS_AT_LEAST_4_DIGIT CHECK (CUST_NO LIKE 'C____'),
        CUST_NAME VARCHAR(50),
        CITY VARCHAR(50) CONSTRAINT CUSTOMER_NOT_BELONG_TO_PESHAVAR CHECK (UPPER (CITY) != 'PESHAVAR'),
        RATING VARCHAR(50) CONSTRAINT RATING_MUST_BE_GREATERTHAN_100_AND_LESSTHAN_200 CHECK (RATING >= 100 AND Rating <= 200),
        S_NO NUMBER(10) REFERENCES Salespeople 
);

CREATE TABLE Orders (
        ORD_NO NUMBER(10),
        AMT NUMBER(10),
        O_DATE TIMESTAMP DEFAULT SYSDATE,
        CUST_NO VARCHAR(5) REFERENCES Customer,
        S_NO NUMBER(10) REFERENCES Salespeople 
);

