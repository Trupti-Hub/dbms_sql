CREATE TABLE Competition (
        COMP_CODE VARCHAR2(5) PRIMARY KEY CONSTRAINT COMP_CODE_MUST_START_WITH_C_AND_MUST_CONTAINS_4_DIGIT CHECK (COMP_CODE LIKE 'C____'),
        COMP_NAME VARCHAR2(15) CONSTRAINT COMP_NAME_MUST_BE_FROM_GK_CODING_QUIZ_PAINTING_SINGING_DESIGNING CHECK (UPPER(COMP_NAME) IN ('GK','CODING','QUIZ','PAINTING','SINGING','DESIGNING'))
);

CREATE TABLE Participants (
        PART_NO VARCHAR2(5) PRIMARY KEY CONSTRAINT PART_NO_MUST_START_WITH_P_AND_MUST_CONTAINS_4_DIGIT CHECK (PART_NO LIKE 'P____'),
        PART_NAME VARCHAR2(30),
        DOB TIMESTAMP,
        EMAIL_ID VARCHAR2(50) UNIQUE CONSTRAINT MUST_BE_ENTER_VALID_AND_UNIQUE_EMAIL_ID CHECK(EMAIL_ID LIKE '__%@__%.__%'),
        CONTACT_NUMBER NUMBER(10) UNIQUE CONSTRAINT MUST_BE_ENTER_VALID_AND_UNIQUE_CONTACT_NUMBER CHECK (REGEXP_LIKE(CONTACT_NUMBER,'^[89][0-9]{9}$'))
);

CREATE TABLE Socrecard (
        SCORE_NO NUMBER(10) PRIMARY KEY,
        PART_NO VARCHAR2(5) REFERENCES Participants,
        COMP_CODE VARCHAR2(5) REFERENCES Competition,
        JUDGE_NO NUMBER(2) CONSTRAINT JUDGE_NO_SHOULD_BE_BETWEEN_1_TO_3 CHECK ((JUDGE_NO) IN (1,2,3)),
        MARKS NUMBER(10)
);

